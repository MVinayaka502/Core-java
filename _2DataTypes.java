public class _2DataTypes {

    public static void main(String[] args){
        // Data types in java


        //integer values
        byte age=20;// Age  1 byte
        System.out.println("Byte = "+age);
        short s=2025 ;// year 2 bytes
        System.out.println("Short = "+s);
        int i=600000; // salary 4 bytes
        System.out.println("Int = "+i);
        long l=140000000000L;// population 8 bytes
        System.out.println("Long = "+l);
        System.out.println();

        //Floating Vlaues

        float f=3.142f;// defing decimal integers  4 bytes
        System.out.println("Float ="+ f);
        double d=5.889009898;// double 8 bytes
        System.out.println("Double = "+d);
        System.out.println();


        // Char Values
        char c='M';// character is defined inside single quote 1 char
        System.out.println("Char c="+c);

        //Boolean values
        //boolean datatypes give the output as true or false
        int aa=10, bb=20;
        if(aa>bb){
            System.out.println("Boolean = True");
        }
        else{
            System.out.println("Boolean = False");
        }



    }
}
